print('Welcome to Treasure Island!!!')
print("Your task is to find the treasure.\n")

left_or_right = input("You are at a crossroad. Where would you like to go 'left' or 'right': ").lower()

if left_or_right != "left":
    print("\nFell into a hole. Game Over")
else:
    swim_or_wait = input("You are near a river. Would you like to 'swim' or 'wait': ").lower()
    if swim_or_wait != "wait":
        print("\nAttacked by trout. Game over")
    else:
        door = input("You have three doors in front of you. Which one would you open 'red', 'yellow', or 'blue': ").lower()
        if door == "red":
            print("\nBurned by Fire. Game Over")
        elif door == "blue":
            print("\nEaten by beast. Game Over")
        elif door == "yellow":
            print("\nYou Win!!!")
        else:
            print("\nGame Over")